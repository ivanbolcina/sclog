use sclog;

CREATE TABLE `SC_LOG` (
  `ID` int(20) unsigned NOT NULL AUTO_INCREMENT,
  `VERSION` int(20) unsigned not null,
  `TS_INS` timestamp NOT NULL, 
  `TS_AUDIT` timestamp NOT NULL,
  `COMPONENT` varchar(100) DEFAULT NULL,  
  `LOG_LEVEL` varchar(20) DEFAULT NULL, 
  `TRACK_ID` varchar(100) DEFAULT NULL, 
  `USER_ID` varchar(100) DEFAULT NULL,
  `REC_SIGNATURE` BLOB DEFAULT NULL,
  `PREV_SIGNATURE` BLOB DEFAULT NULL,
  `MESSAGE` varchar(2000) DEFAULT NULL,
  `PREV_ID` int(20) DEFAULT NULL,
  `CUSTOM` varchar(2000) DEFAULT NULL,    
  `KEY_NAME` varchar(100) DEFAULT NULL,
  PRIMARY KEY (`ID`,TS_INS)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;
